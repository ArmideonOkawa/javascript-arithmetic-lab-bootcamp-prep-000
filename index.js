function add(a,b){
  var num = a + b;
  return num;
}

function subtract(a,b){
  var newNum = a - b;
  return newNum;
}

function multiply(a,b){
  var multi = a * b;
  return multi;
}

function divide(a,b){
  var div = a / b;
  return div;
}

